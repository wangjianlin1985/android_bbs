package com.chengxusheji.action;

import java.io.File;
import java.io.IOException;
import java.io.OutputStream;
import java.text.SimpleDateFormat;
import java.util.ArrayList;
import org.apache.struts2.ServletActionContext;
import org.springframework.context.annotation.Scope;
import org.springframework.stereotype.Controller;
import java.util.List;
import javax.annotation.Resource;
import javax.servlet.http.HttpServletResponse;
import com.opensymphony.xwork2.ActionContext;
import com.chengxusheji.dao.CollegeDAO;
import com.chengxusheji.domain.College;
import com.chengxusheji.utils.FileTypeException;
import com.chengxusheji.utils.ExportExcelUtil;

@Controller @Scope("prototype")
public class CollegeAction extends BaseAction {

    /*当前第几页*/
    private int currentPage;
    public void setCurrentPage(int currentPage) {
        this.currentPage = currentPage;
    }
    public int getCurrentPage() {
        return currentPage;
    }

    /*一共多少页*/
    private int totalPage;
    public void setTotalPage(int totalPage) {
        this.totalPage = totalPage;
    }
    public int getTotalPage() {
        return totalPage;
    }

    private String collegeNumber;
    public void setCollegeNumber(String collegeNumber) {
        this.collegeNumber = collegeNumber;
    }
    public String getCollegeNumber() {
        return collegeNumber;
    }

    /*当前查询的总记录数目*/
    private int recordNumber;
    public void setRecordNumber(int recordNumber) {
        this.recordNumber = recordNumber;
    }
    public int getRecordNumber() {
        return recordNumber;
    }

    /*业务层对象*/
    @Resource CollegeDAO collegeDAO;

    /*待操作的College对象*/
    private College college;
    public void setCollege(College college) {
        this.college = college;
    }
    public College getCollege() {
        return this.college;
    }

    /*跳转到添加College视图*/
    public String AddView() {
        ActionContext ctx = ActionContext.getContext();
        return "add_view";
    }

    /*添加College信息*/
    @SuppressWarnings("deprecation")
    public String AddCollege() {
        ActionContext ctx = ActionContext.getContext();
        /*验证学院编号是否已经存在*/
        String collegeNumber = college.getCollegeNumber();
        College db_college = collegeDAO.GetCollegeByCollegeNumber(collegeNumber);
        if(null != db_college) {
            ctx.put("error",  java.net.URLEncoder.encode("该学院编号已经存在!"));
            return "error";
        }
        try {
            collegeDAO.AddCollege(college);
            ctx.put("message",  java.net.URLEncoder.encode("College添加成功!"));
            return "add_success";
        } catch(FileTypeException ex) {
        	ctx.put("error",  java.net.URLEncoder.encode("图片文件格式不对!"));
            return "error";
        } catch (Exception e) {
            e.printStackTrace();
            ctx.put("error",  java.net.URLEncoder.encode("College添加失败!"));
            return "error";
        }
    }

    /*查询College信息*/
    public String QueryCollege() {
        if(currentPage == 0) currentPage = 1;
        List<College> collegeList = collegeDAO.QueryCollegeInfo(currentPage);
        /*计算总的页数和总的记录数*/
        collegeDAO.CalculateTotalPageAndRecordNumber();
        /*获取到总的页码数目*/
        totalPage = collegeDAO.getTotalPage();
        /*当前查询条件下总记录数*/
        recordNumber = collegeDAO.getRecordNumber();
        ActionContext ctx = ActionContext.getContext();
        ctx.put("collegeList",  collegeList);
        ctx.put("totalPage", totalPage);
        ctx.put("recordNumber", recordNumber);
        ctx.put("currentPage", currentPage);
        return "query_view";
    }

    /*后台导出到excel*/
    public String QueryCollegeOutputToExcel() { 
        List<College> collegeList = collegeDAO.QueryCollegeInfo();
        ExportExcelUtil ex = new ExportExcelUtil();
        String title = "College信息记录"; 
        String[] headers = { "学院编号","学院名称"};
        List<String[]> dataset = new ArrayList<String[]>(); 
        for(int i=0;i<collegeList.size();i++) {
        	College college = collegeList.get(i); 
        	dataset.add(new String[]{college.getCollegeNumber(),college.getCollegeName()});
        }
        /*
        OutputStream out = null;
		try {
			out = new FileOutputStream("C://output.xls");
			ex.exportExcel(title,headers, dataset, out);
		    out.close();
		} catch (Exception e) {
			e.printStackTrace();
		}
		*/
		HttpServletResponse response = null;//创建一个HttpServletResponse对象 
		OutputStream out = null;//创建一个输出流对象 
		try { 
			response = ServletActionContext.getResponse();//初始化HttpServletResponse对象 
			out = response.getOutputStream();//
			response.setHeader("Content-disposition","attachment; filename="+"College.xls");//filename是下载的xls的名，建议最好用英文 
			response.setContentType("application/msexcel;charset=UTF-8");//设置类型 
			response.setHeader("Pragma","No-cache");//设置头 
			response.setHeader("Cache-Control","no-cache");//设置头 
			response.setDateHeader("Expires", 0);//设置日期头  
			String rootPath = ServletActionContext.getServletContext().getRealPath("/");
			ex.exportExcel(rootPath,title,headers, dataset, out);
			out.flush();
		} catch (IOException e) { 
			e.printStackTrace(); 
		}finally{
			try{
				if(out!=null){ 
					out.close(); 
				}
			}catch(IOException e){ 
				e.printStackTrace(); 
			} 
		}
		return null;
    }
    /*前台查询College信息*/
    public String FrontQueryCollege() {
        if(currentPage == 0) currentPage = 1;
        List<College> collegeList = collegeDAO.QueryCollegeInfo(currentPage);
        /*计算总的页数和总的记录数*/
        collegeDAO.CalculateTotalPageAndRecordNumber();
        /*获取到总的页码数目*/
        totalPage = collegeDAO.getTotalPage();
        /*当前查询条件下总记录数*/
        recordNumber = collegeDAO.getRecordNumber();
        ActionContext ctx = ActionContext.getContext();
        ctx.put("collegeList",  collegeList);
        ctx.put("totalPage", totalPage);
        ctx.put("recordNumber", recordNumber);
        ctx.put("currentPage", currentPage);
        return "front_query_view";
    }

    /*查询要修改的College信息*/
    public String ModifyCollegeQuery() {
        ActionContext ctx = ActionContext.getContext();
        /*根据主键collegeNumber获取College对象*/
        College college = collegeDAO.GetCollegeByCollegeNumber(collegeNumber);

        ctx.put("college",  college);
        return "modify_view";
    }

    /*查询要修改的College信息*/
    public String FrontShowCollegeQuery() {
        ActionContext ctx = ActionContext.getContext();
        /*根据主键collegeNumber获取College对象*/
        College college = collegeDAO.GetCollegeByCollegeNumber(collegeNumber);

        ctx.put("college",  college);
        return "front_show_view";
    }

    /*更新修改College信息*/
    public String ModifyCollege() {
        ActionContext ctx = ActionContext.getContext();
        try {
            collegeDAO.UpdateCollege(college);
            ctx.put("message",  java.net.URLEncoder.encode("College信息更新成功!"));
            return "modify_success";
        } catch (Exception e) {
            e.printStackTrace();
            ctx.put("error",  java.net.URLEncoder.encode("College信息更新失败!"));
            return "error";
       }
   }

    /*删除College信息*/
    public String DeleteCollege() {
        ActionContext ctx = ActionContext.getContext();
        try { 
            collegeDAO.DeleteCollege(collegeNumber);
            ctx.put("message",  java.net.URLEncoder.encode("College删除成功!"));
            return "delete_success";
        } catch (Exception e) { 
            e.printStackTrace();
            ctx.put("error",  java.net.URLEncoder.encode("College删除失败!"));
            return "error";
        }
    }

}
